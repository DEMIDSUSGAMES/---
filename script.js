// –°–æ—Å—Ç–æ—è–Ω–∏–µ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
let currentFanfic = '';
let currentChapterIndex = 0;

// –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ
document.addEventListener('DOMContentLoaded', function() {
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —Å–æ—Ö—Ä–∞–Ω—ë–Ω–Ω–æ–≥–æ –ø—Ä–æ–≥—Ä–µ—Å—Å–∞
    loadProgress();
    
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞—Å—Ç—Ä–æ–π–∫—É —Ç—ë–º–Ω–æ–π —Ç–µ–º—ã
    if (localStorage.getItem('darkMode') === 'enabled') {
        document.body.classList.add('dark-mode');
        updateDarkModeButton();
    }
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –ø—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä
    updateProgressBar();
});

// –ó–∞–≥—Ä—É–∑–∫–∞ –ø—Ä–æ–≥—Ä–µ—Å—Å–∞
function loadProgress() {
    const saved = localStorage.getItem('fanficProgress_dimamika');
    if (saved) {
        const progress = JSON.parse(saved);
        currentFanfic = progress.fanficId;
        currentChapterIndex = progress.chapterIndex;
        console.log('–ó–∞–≥—Ä—É–∂–µ–Ω –ø—Ä–æ–≥—Ä–µ—Å—Å:', progress);
        return true;
    }
    return false;
}

// –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –ø—Ä–æ–≥—Ä–µ—Å—Å–∞
function saveProgress() {
    if (currentFanfic) {
        const progress = {
            fanficId: currentFanfic,
            chapterIndex: currentChapterIndex,
            timestamp: new Date().toISOString()
        };
        localStorage.setItem('fanficProgress_' + currentFanfic, JSON.stringify(progress));
        console.log('–ü—Ä–æ–≥—Ä–µ—Å—Å —Å–æ—Ö—Ä–∞–Ω—ë–Ω:', progress);
    }
}

// –ü–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏–µ —Å—Ç—Ä–∞–Ω–∏—Ü
function showPage(pageId) {
    // –°–∫—Ä—ã–≤–∞–µ–º –≤—Å–µ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
    document.querySelectorAll('.page').forEach(page => {
        page.classList.remove('active-page');
    });
    
    // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –Ω—É–∂–Ω—É—é —Å—Ç—Ä–∞–Ω–∏—Ü—É
    const targetPage = document.getElementById('page-' + pageId);
    if (targetPage) {
        targetPage.classList.add('active-page');
    }
    
    // –ï—Å–ª–∏ –æ—Ç–∫—Ä—ã–≤–∞–µ–º —á–∏—Ç–∞–ª–∫—É, –æ–±–Ω–æ–≤–ª—è–µ–º –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ
    if (pageId === 'reading' && currentFanfic) {
        updateChapterDisplay();
    }
    
    // –ü—Ä–æ–∫—Ä—É—Ç–∫–∞ –≤–≤–µ—Ä—Ö
    window.scrollTo(0, 0);
}

// –ù–∞—á–∞—Ç—å —á—Ç–µ–Ω–∏–µ
function startReading(fanficId) {
    currentFanfic = fanficId;
    
    // –ï—Å–ª–∏ –Ω–µ—Ç —Å–æ—Ö—Ä–∞–Ω—ë–Ω–Ω–æ–≥–æ –ø—Ä–æ–≥—Ä–µ—Å—Å–∞, –Ω–∞—á–∏–Ω–∞–µ–º —Å –ø–µ—Ä–≤–æ–π –≥–ª–∞–≤—ã
    if (!loadProgress()) {
        currentChapterIndex = 0;
    }
    
    showPage('reading');
}

// –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –≥–ª–∞–≤—ã
function updateChapterDisplay() {
    const fanfic = fanficData[currentFanfic];
    if (!fanfic) return;
    
    const chapter = fanfic.chapters[currentChapterIndex];
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ –∏ —Ç–µ–∫—Å—Ç
    document.getElementById('chapter-title').textContent = chapter.title;
    document.getElementById('chapter-text').innerHTML = chapter.content;
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –∏–Ω–¥–∏–∫–∞—Ç–æ—Ä –ø—Ä–æ–≥—Ä–µ—Å—Å–∞
    const totalChapters = fanfic.chapters.length;
    document.getElementById('chapter-indicator').innerHTML = `
        <span class="progress-text">–ì–ª–∞–≤–∞ ${currentChapterIndex + 1} –∏–∑ ${totalChapters}</span>
        <div class="progress-bar">
            <div class="progress-fill" id="progress-fill"></div>
        </div>
    `;
    
    // –û–±–Ω–æ–≤–ª—è–µ–º –ø—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä
    updateProgressBar();
    
    // –£–ø—Ä–∞–≤–ª—è–µ–º –≤–∏–¥–∏–º–æ—Å—Ç—å—é –∫–Ω–æ–ø–æ–∫ –Ω–∞–≤–∏–≥–∞—Ü–∏–∏
    const prevBtn = document.getElementById('prev-btn');
    const nextBtn = document.getElementById('next-btn');
    
    if (prevBtn) {
        prevBtn.style.display = currentChapterIndex > 0 ? 'block' : 'none';
    }
    
    if (nextBtn) {
        nextBtn.style.display = currentChapterIndex < totalChapters - 1 ? 'block' : 'none';
    }
    
    // –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø—Ä–æ–≥—Ä–µ—Å—Å
    saveProgress();
}

// –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –ø—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä–∞
function updateProgressBar() {
    const fanfic = fanficData[currentFanfic];
    if (!fanfic) return;
    
    const totalChapters = fanfic.chapters.length;
    const progress = ((currentChapterIndex + 1) / totalChapters) * 100;
    
    const progressFill = document.getElementById('progress-fill');
    if (progressFill) {
        progressFill.style.width = `${progress}%`;
    }
}

// –°–º–µ–Ω–∞ –≥–ª–∞–≤—ã
function changeChapter(direction) {
    const fanfic = fanficData[currentFanfic];
    if (!fanfic) return;
    
    const newIndex = currentChapterIndex + direction;
    
    if (newIndex >= 0 && newIndex < fanfic.chapters.length) {
        currentChapterIndex = newIndex;
        updateChapterDisplay();
        
        // –ü—Ä–æ–∫—Ä—É—Ç–∫–∞ –∫ –≤–µ—Ä—Ö—É –∫–æ–Ω—Ç–µ–Ω—Ç–∞
        document.getElementById('chapter-text').scrollTop = 0;
    }
}

// –í–æ–∑–≤—Ä–∞—Ç –≤ –º–µ–Ω—é
function returnToMenu() {
    if (confirm('–í–µ—Ä–Ω—É—Ç—å—Å—è –≤ –≥–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é? –í–∞—à –ø—Ä–æ–≥—Ä–µ—Å—Å –±—É–¥–µ—Ç —Å–æ—Ö—Ä–∞–Ω—ë–Ω.')) {
        showPage('menu');
    }
}

// –ü–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏–µ —Ç—ë–º–Ω–æ–≥–æ —Ä–µ–∂–∏–º–∞
function toggleDarkMode() {
    document.body.classList.toggle('dark-mode');
    
    // –°–æ—Ö—Ä–∞–Ω—è–µ–º –Ω–∞—Å—Ç—Ä–æ–π–∫—É
    if (document.body.classList.contains('dark-mode')) {
        localStorage.setItem('darkMode', 'enabled');
    } else {
        localStorage.setItem('darkMode', 'disabled');
    }
    
    updateDarkModeButton();
}

