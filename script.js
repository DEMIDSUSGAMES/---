// Состояние приложения
let currentAuthor = '';
let currentFanfic = '';
let currentChapterIndex = 0;

// Инициализация при загрузке
document.addEventListener('DOMContentLoaded', function() {
    // Проверяем наличие сохранённого прогресса
    loadProgress();
    
    // Проверяем настройку тёмной темы
    if (localStorage.getItem('darkMode') === 'enabled') {
        document.body.classList.add('dark-mode');
        updateDarkModeButton();
    }
    
    // Запрещаем скролл на главной странице
    document.getElementById('page-menu').addEventListener('scroll', preventScroll);
});

// Предотвращение скролла
function preventScroll(e) {
    e.preventDefault();
    e.stopPropagation();
    return false;
}

// Показать уведомление
function showNotification(message, type = 'info') {
    const container = document.getElementById('notification-container');
    if (!container) return;
    
    const notification = document.createElement('div');
    notification.className = `notification ${type}`;
    notification.textContent = message;
    
    container.appendChild(notification);
    
    // Удалить уведомление после анимации
    setTimeout(() => {
        if (notification.parentNode === container) {
            container.removeChild(notification);
        }
    }, 3000);
}

// Загрузка прогресса
function loadProgress() {
    const saved = localStorage.getItem('fanficProgress');
    if (saved) {
        try {
            const progress = JSON.parse(saved);
            currentAuthor = progress.author || '';
            currentFanfic = progress.fanfic || '';
            currentChapterIndex = progress.chapterIndex || 0;
            console.log('Прогресс загружен');
            return true;
        } catch (e) {
            console.error('Ошибка загрузки прогресса:', e);
        }
    }
    return false;
}

// Сохранение прогресса
function saveProgress() {
    if (currentAuthor && currentFanfic) {
        const progress = {
            author: currentAuthor,
            fanfic: currentFanfic,
            chapterIndex: currentChapterIndex,
            timestamp: new Date().toISOString()
        };
        localStorage.setItem('fanficProgress', JSON.stringify(progress));
    }
}

// Переключение страниц
function showPage(pageId) {
    // Скрываем все страницы
    document.querySelectorAll('.page').forEach(page => {
        page.classList.remove('active-page');
    });
    
    // Показываем нужную страницу
    const targetPage = document.getElementById('page-' + pageId);
    if (targetPage) {
        targetPage.classList.add('active-page');
        targetPage.scrollTop = 0;
    }
    
    // Прокрутка вверх
    window.scrollTo(0, 0);
    
    // Показать уведомление при возврате в меню
    if (pageId === 'menu') {
        showNotification('Добро пожаловать в главное меню', 'success');
    }
}

// Начать чтение
function startReading(author, fanficId) {
    currentAuthor = author;
    currentFanfic = fanficId;
    currentChapterIndex = 0;
    
    showPage('reading');
    updateChapterDisplay();
    
    // Показать уведомление
    const fanfic = fanficData[author].fanfics[fanficId];
    showNotification(`Начато чтение: "${fanfic.title}"`, 'success');
}

// Обновление отображения главы
function updateChapterDisplay() {
    const authorData = fanficData[currentAuthor];
    if (!authorData || !authorData.fanfics || !authorData.fanfics[currentFanfic]) {
        console.error('Фанфик не найден:', currentAuthor, currentFanfic);
        showNotification('Ошибка загрузки фанфика', 'warning');
        showPage('menu');
        return;
    }
    
    const fanfic = authorData.fanfics[currentFanfic];
    const chapter = fanfic.chapters[currentChapterIndex];
    
    if (!chapter) {
        console.error('Глава не найдена:', currentChapterIndex);
        showNotification('Ошибка загрузки главы', 'warning');
        return;
    }
    
    // Обновляем заголовок и текст
    document.getElementById('chapter-title').textContent = chapter.title;
    document.getElementById('chapter-text').innerHTML = chapter.content;
    
    // Обновляем индикатор прогресса
    const totalChapters = fanfic.chapters.length;
    document.getElementById('chapter-indicator').innerHTML = `
        <span class="progress-text">Глава ${currentChapterIndex + 1} из ${totalChapters}</span>
        <div class="progress-bar">
            <div class="progress-fill" id="progress-fill"></div>
        </div>
    `;
    
    // Обновляем прогресс-бар
    updateProgressBar();
    
    // Управляем видимостью кнопок навигации
    const prevBtn = document.getElementById('prev-btn');
    const nextBtn = document.getElementById('next-btn');
    
    if (prevBtn) {
        prevBtn.style.display = currentChapterIndex > 0 ? 'flex' : 'none';
        prevBtn.disabled = currentChapterIndex === 0;
    }
    
    if (nextBtn) {
        nextBtn.style.display = currentChapterIndex < totalChapters - 1 ? 'flex' : 'none';
        nextBtn.disabled = currentChapterIndex === totalChapters - 1;
    }
    
    // Сохраняем прогресс
    saveProgress();
}

// Обновление прогресс-бара
function updateProgressBar() {
    const authorData = fanficData[currentAuthor];
    if (!authorData || !authorData.fanfics || !authorData.fanfics[currentFanfic]) return;
    
    const fanfic = authorData.fanfics[currentFanfic];
    const totalChapters = fanfic.chapters.length;
    const progress = ((currentChapterIndex + 1) / totalChapters) * 100;
    
    const progressFill = document.getElementById('progress-fill');
    if (progressFill) {
        progressFill.style.width = `${progress}%`;
    }
}

// Смена главы
function changeChapter(direction) {
    const authorData = fanficData[currentAuthor];
    if (!authorData || !authorData.fanfics || !authorData.fanfics[currentFanfic]) return;
    
    const fanfic = authorData.fanfics[currentFanfic];
    const newIndex = currentChapterIndex + direction;
    
    if (newIndex >= 0 && newIndex < fanfic.chapters.length) {
        currentChapterIndex = newIndex;
        updateChapterDisplay();
        
        // Прокрутка к верху контента
        document.getElementById('chapter-text').scrollTop = 0;
        
        // Показать уведомление о смене главы
        const chapterTitle = fanfic.chapters[currentChapterIndex].title;
        showNotification(`Переход к: ${chapterTitle}`, 'info');
    }
}

// Возврат в меню
function returnToMenu() {
    showPage('menu');
    showNotification('Возврат в главное меню. Прогресс сохранён.', 'success');
}

// Переключение тёмного режима
function toggleDarkMode() {
    document.body.classList.toggle('dark-mode');
    
    // Сохраняем настройку
    if (document.body.classList.contains('dark-mode')) {
        localStorage.setItem('darkMode', 'enabled');
        showNotification('Ночной режим включён', 'info');
    } else {
        localStorage.setItem('darkMode', 'disabled');
        showNotification('Дневной режим включён', 'info');
    }
    
    updateDarkModeButton();
}

// Обновление текста кнопки тёмного режима
function updateDarkModeButton() {
    const btn = document.getElementById('dark-mode-btn');
    if (btn) {
        if (document.body.classList.contains('dark-mode')) {
            btn.innerHTML = '☀️ Дневной режим';
        } else {
            btn.innerHTML = '🌙 Ночной режим';
        }
    }
}

